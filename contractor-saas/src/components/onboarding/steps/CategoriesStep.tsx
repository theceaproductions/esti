export default function CategoriesStep() {
  const { data, updateData } = useOnboarding();
  const [customCategory, setCustomCategory] = useState('');

  const toggleCategory = (categoryId: string) => {
    const newSelected = data.selectedCategories.includes(categoryId)
      ? data.selectedCategories.filter((id) => id !== categoryId)
      : [...data.selectedCategories, categoryId];
    updateData({ selectedCategories: newSelected });
  };

  const addCustomCategory = () => {
    if (customCategory.trim()) {
      const newId = `custom-${Date.now()}`;
      toggleCategory(newId);
      setCustomCategory('');
    }
  };

  return (
    <div className="space-y-6">
      <div className="text-center mb-8">
        <h2 className="text-2xl font-bold text-gray-900">What services do you offer?</h2>
        <p className="text-gray-600 mt-2">Select the categories that match your business</p>
      </div>

      {/* Categories Grid */}
      <div className="grid grid-cols-2 md:grid-cols-4 gap-4">
        {SERVICE_CATEGORIES.map((category) => {
          const isSelected = data.selectedCategories.includes(category.id);
          return (
            <button
              key={category.id}
              onClick={() => toggleCategory(category.id)}
              className={`relative p-4 rounded-xl border-2 transition-all duration-200 ${
                isSelected
                  ? 'border-blue-500 bg-blue-50 shadow-md'
                  : 'border-gray-200 hover:border-gray-300 hover:shadow-sm'
              }`}
            >
              <div className={`mb-2 ${isSelected ? 'text-blue-600' : 'text-gray-500'}`}>
                {iconMap[category.icon] || iconMap.Wrench}
              </div>
              <div className="font-medium text-gray-900">{category.name}</div>
              <div className="text-xs text-gray-500 mt-1">{category.description}</div>
              {isSelected && (
                <div className="absolute top-2 right-2 w-5 h-5 bg-blue-500 rounded-full flex items-center justify-center">
                  <Check className="w-3 h-3 text-white" />
                </div>
              )}
            </button>
          );
        })}
      </div>

      {/* Custom Category */}
      <div className="mt-8 pt-6 border-t border-gray-200">
        <h3 className="text-sm font-medium text-gray-700 mb-3">Add a custom category</h3>
        <div className="flex gap-3">
          <input
            type="text"
            value={customCategory}
            onChange={(e) => setCustomCategory(e.target.value)}
            placeholder="Enter category name"
            className="flex-1 px-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
            onKeyDown={(e) => e.key === 'Enter' && addCustomCategory()}
          />
          <button
            onClick={addCustomCategory}
            className="px-4 py-2 bg-gray-100 text-gray-700 rounded-lg hover:bg-gray-200 flex items-center gap-2"
          >
            <Plus className="w-4 h-4" />
            Add
          </button>
        </div>
      </div>

      {/* Selected Count */}
      <div className="text-center text-sm text-gray-500">
        {data.selectedCategories.length > 0
          ? `${data.selectedCategories.length} category selected`
          : 'Select at least one category to continue'}
      </div>
    </div>
  );
}
